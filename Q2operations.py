a = int(input("Enter first number: "))  # Read the first integer from the user
b = int(input("Enter second number: "))  # Read the second integer from the user

print("\n--- Results ---")  # Display a heading for the results
print("Addition:", a + b)  # Show the sum of the two numbers
print("Multiplication:", a * b)  # Show the product of the two numbers
print("Division:", a / b)  # Show the division result (a divided by b)
print("Modulus:", a % b)  # Show the remainder when a is divided by b
print("Exponentiation:", a ** b)  # Show a raised to the power of b
