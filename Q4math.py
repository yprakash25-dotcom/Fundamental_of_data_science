# Q4_math.py  # File label for the math operations script

import math  # Import math functions for logarithms

num = float(input("Enter a number: "))  # Read a floating-point number

print("Cube:", num ** 3)  # Show the cube of the number
print("Cube Root:", num ** (1/3))  # Show the cube root of the number
print("Natural Log:", math.log(num))  # Show the natural logarithm of the number
print("Log base 2:", math.log2(num))  # Show the base-2 logarithm of the number
print("Power of 6:", num ** 6)  # Show the number raised to the 6th power
